static void print_logo_narrow(void) {
    oled_set_cursor(0, 0);
    render_logo();

    oled_set_cursor(0, 6);
    /* Print current layer */
    oled_write("LAYER", false);
    switch (get_highest_layer(layer_state)) {
        case U_BASE:
            oled_write("_BASE", false);
            break;
        case U_NUM:
            oled_write("_NUM_", false);
            break;
        case U_SYM:
            oled_write("_SYM_", false);
            break;
        case U_NAV:
            oled_write("_NAV_", false);
            break;
        case U_MEDIA:
            oled_write("_MEDI", false);
            break;
        default:
            oled_write("_Undf", false);
    }
    
    render_luna(0, 10);


    oled_set_cursor(0, 15);

    switch (get_highest_layer(default_layer_state)) {
        case U_BASE:
            oled_write("QWRTY", false);
            break;
        case U_GAME:
            oled_write("_GAME", false);
            break;
        default:
            oled_write("_UNDE", false);
    }

}


